<?php

namespace Yitznewton\TwentyFortyEight;

class GridFlipper
{
    /**
     * @param array $grid
     * @return array
     */
    public function flip(array $grid)
    {
        return $grid;
    }

    /**
     * @param array $grid
     * @return array
     */
    public function unflip(array $grid)
    {
        return $grid;
    }
}
